const int N = 1e5 + 9;

int spf[N];
vector<int> primes;
void sieve() {
  for(int i = 2; i < N; i++) {
    if (spf[i] == 0) spf[i] = i, primes.push_back(i);
    int sz = primes.size();
    for (int j = 0; j < sz && i * primes[j] < N && primes[j] <= spf[i]; j++) {
      spf[i * primes[j]] = primes[j];
    }
  }
}

void solve() {
    int n; cin >> n;
    sieve();
    for (int prime : primes) {
        if (prime >= n) {
            cout << prime << endl;
            return;
        }
    }
}

